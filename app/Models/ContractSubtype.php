 *          type="integer",
 *          format="int32"
 *      )
 * )
 */
class ContractSubtype extends BaseModel
{
    use SoftDeletes;

    use HasFactory;

    public $table = 'contract_subtypes';

    protected array $dates = ['deleted_at'];

    public $fillable = [
        'name',
        'type',
        'description',
        'created_by',
        'updated_by'
    ];

    /**
     * The attributes that should be cast to native types.
     *
     * @var array
     */
    protected $casts = [
        'name' => 'string',
        'description' => 'string',
        'type' => 'string',
        'created_by' => 'integer',
        'updated_by' => 'integer'
    ];

    /**
     * Validation rules
     *
     * @var array
     */
    public static array $rules = [
        'name' => 'required',
        'type' => 'required',
    ];

    /**
     * @return HasMany
     **/
    public function institutions() : HasMany
    {
        return $this->hasMany(Institution::class);
    }
}

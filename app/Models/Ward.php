 *          readOnly=$FIELD_READ_ONLY$,
 *          nullable=$FIELD_NULLABLE$,
 *          type="integer",
 *          format="int32"
 *      )
 * )
 */
class Ward extends BaseModel
{
    use SoftDeletes;

    use HasFactory;

    public $table = 'wards';


    protected array $dates = ['deleted_at'];



    public $fillable = [
        'name',
        'district_id',
        'created_by',
        'updated_by'
    ];

    /**
     * The attributes that should be cast to native types.
     *
     * @var array
     */
    protected $casts = [
        'id' => 'integer',
        'name' => 'string',
        'district_id' => 'integer',
        'created_by' => 'integer',
        'updated_by' => 'integer'
    ];

    /**
     * Validation rules
     *
     * @var array
     */
    public static array $rules = [
        'name' => 'required',
        'district_id' => 'required'
    ];

    /**
     * @return BelongsTo
     **/
    public function district(): BelongsTo
    {
        return $this->belongsTo(District::class);
    }
}
